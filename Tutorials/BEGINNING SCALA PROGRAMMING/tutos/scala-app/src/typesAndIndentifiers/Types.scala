//Byte
val byteDefinition:Byte = 127; //Bytes can go to all the way up to 127 as in java
println(byteDefinition);
val alsoYouCanDefineAByteLikeThis = 56:Byte;

//short
val shortValDefinition:Short = 32176;
val anotherShortDefinition = 32452:Short;
println(anotherShortDefinition);

//Int
//int is the default type
val intIsTheDefaultType = 450;
val alsoIntCanBeSpecified:Int = 450;
println(alsoIntCanBeSpecified);

//Long
val aLongValDefinition = 340L;
val anotherLongValDefinition:Long = 340;
val alsoYouCanUseSimpleL = 567l;
println(alsoYouCanUseSimpleL);

//Float
val aFlotValue = 56.23f;
val flotValDefinition:Float = 34.21f;
println(flotValDefinition); // You always have to add a f to indicate its a float value


//Double
//The default floating point type
val doubleVal = 34.22;
